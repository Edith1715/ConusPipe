#!/usr/bin/perl -w 
use strict;
#-----------------------------------------------------------------------------
#----------------------------------- MAIN ------------------------------------
#-----------------------------------------------------------------------------
my $usage = "\n\nUSAGE: extractHmmerHits.pl <hmm.out> \n";


die($usage) unless $ARGV[0];

my $file = $ARGV[0];
my ($name)=$file=~ /(\S+)\.out/;
open(FH,'<',$file) || die"can't open $file\n";
open OUT1,'>',$name.".hit";
while (defined(my $line=<FH>)){
	last if $line=~ /Domain/;
	if ($line=~ /frame/){
		chomp ($line);
		my@h= split /\s+/,$line;
		print OUT1 "$h[9]\t$h[2]\n";

	}
}

close(FH);
close(OUT1);
	

	
